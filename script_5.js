      // ═══ 키워드 분석 엔진 ═══
      function analyzeKemetKeyword(input) {
        const lowerInput = input.toLowerCase();
        let bestCategory = null;
        let bestScore = 0;

        for (const [catKey, catData] of Object.entries(KEMET_KEYWORD_MAP)) {
          let score = 0;
          const matchedWords = [];
          for (const kw of catData.keywords) {
            if (lowerInput.includes(kw)) {
              score += kw.length; // 긴 키워드에 더 높은 가중치
              matchedWords.push(kw);
            }
          }
          if (score > bestScore) {
            bestScore = score;
            bestCategory = { key: catKey, ...catData, matchedWords };
          }
        }

        return bestCategory; // null이면 범용 조언
      }

      // ═══ 키워드 기반 신 가중 선택 ═══
      function selectGodByKeyword(category) {
        if (!category || !category.godWeight) {
          return KEMET_GODS[Math.floor(Math.random() * KEMET_GODS.length)];
        }

        const weighted = [];
        for (const god of KEMET_GODS) {
          const w = category.godWeight[god.god] || 1;
          for (let i = 0; i < w; i++) weighted.push(god);
        }
        return weighted[Math.floor(Math.random() * weighted.length)];
      }

